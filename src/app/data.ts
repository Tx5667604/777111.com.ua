export interface PartVariant {
  quality: 'copy' | 'original' | 'original_with_frame'
  label: string
  partCost: number
  laborCost: number
}

export interface PhonePartEntry {
  modelCode: string
  modelName: string
  parts: Record<string, PartVariant[]>
}

export interface BrandParts {
  id: string
  name: string
  logo: string
  models: PhonePartEntry[]
}

// Допоміжна функція для створення запису дисплея
function display(copy: number, orig: number, labor: number): PartVariant[] {
  return [
    { quality: 'copy', label: 'Копія', partCost: copy, laborCost: labor },
    { quality: 'original', label: 'Оригінал', partCost: orig, laborCost: labor },
    { quality: 'original_with_frame', label: 'Оригінал з рамкою', partCost: Math.round(orig * 1.3), laborCost: labor + 100 },
  ]
}

// Допоміжна функція для записів із двома варіантами (copy + original)
function two(copy: number, orig: number, labor: number): PartVariant[] {
  return [
    { quality: 'copy', label: 'Копія', partCost: copy, laborCost: labor },
    { quality: 'original', label: 'Оригінал', partCost: orig, laborCost: labor },
  ]
}

// Допоміжна функція для записів тільки з оригіналом
function only(price: number, labor: number): PartVariant[] {
  return [
    { quality: 'original', label: 'Оригінал', partCost: price, laborCost: labor },
  ]
}

